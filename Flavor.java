public enum Flavor {
    CHOCOLATE{
        @Override
        public String toString() {
            return "Chocolate";
        }
    },
    VANILLA{
        @Override
        public String toString() {
            return "Vanilla";
        }
    },
    STRAWBERRY{
        @Override
        public String toString() {
            return "Strawberry";
        }
    },
    BLUEBERRY{
        @Override
        public String toString() {
            return "Blueberry";
        }
    }
}
